import sys


#-------------------------------------------------------------------
#-------------------------------------------------------------------
def get_input(filename):
    l = []
    with open(filename,'r') as f:
        for line in f:
            l.append(line.strip())
    return l


#-------------------------------------------------------------------
# Parse the input and extract the player cards.
#-------------------------------------------------------------------
def get_cards(raw):
    p1 = []
    p2 = []

    i = 1
    while raw[i] != "":
        p1.append(raw[i])
        i += 1

    i += 2
    for l in raw[i:]:
        p2.append(l)

    return (p1, p2)


#-------------------------------------------------------------------
#-------------------------------------------------------------------
def problem1(filename):
    print("Problem 1")
    print("---------")
    raw = get_input(filename)
    (p1, p2) = get_cards(raw)
    print("player 1:", p1)
    print("player 2:", p2)
    print("")


