def get_input(filename):
    l = []
    with open(filename,'r') as f:
        for line in f:
            l.append(line.strip())
    return l


#-------------------------------------------------------------------
#-------------------------------------------------------------------
def get_bags(filename):

    raw_rules = get_input(filename)

    for r in raw_rules:
        (target,  source) = r.split("bags contain")
        sources = source.split(",")
        print(target, "contain", sources)


